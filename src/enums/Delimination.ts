/**
 * Representation of the manner of delimination to be implemented
 */
enum Delimination {
  /**
   * No delimination to be used
   */
  NONE = 1,
  /**
   * Deliminate each character by spaces
   */
  SPACES = 2,
  /**
   * Maintain the original spacing
   */
  ORIGINAL = 3
}

export { Delimination }
